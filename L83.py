def count_ch(x, y):
    total = 0
    random = 0
    while random <= 0:
        random = random + 1
        for ch in x:
            if ch == y:
                total = total + 1
    print(total)


    
count_ch("bonobos", "o")
count_ch("boot", "o")
count_ch("sarah", "a")


